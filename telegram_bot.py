import json
import os

import telebot


# =========================
# TELEGRAM CONFIG
# =========================
# Replace this with your token from @BotFather.
BOT_TOKEN = "YOUR_BOT_TOKEN"

USERS_FILE = "users.json"
LATEST_RESTOCK_FILE = "latest_restocking_alert.json"

bot = telebot.TeleBot(BOT_TOKEN)


# =========================
# FILE HELPERS
# =========================
def ensure_json_file(filename, default_data):
    if not os.path.exists(filename):
        with open(filename, "w", encoding="utf-8") as f:
            json.dump(default_data, f, indent=4)


def load_users():
    ensure_json_file(USERS_FILE, {})
    with open(USERS_FILE, "r", encoding="utf-8") as f:
        return json.load(f)


def save_users(users):
    with open(USERS_FILE, "w", encoding="utf-8") as f:
        json.dump(users, f, indent=4)


def load_latest_restocking_alert():
    if not os.path.exists(LATEST_RESTOCK_FILE):
        return None

    with open(LATEST_RESTOCK_FILE, "r", encoding="utf-8") as f:
        return json.load(f)


# =========================
# BOT COMMANDS
# =========================
@bot.message_handler(commands=["start"])
def start(message):
    bot.reply_to(
        message,
        """
🤖 Welcome to AI Restaurant Procurement Bot

This bot connects your Telegram account with the Streamlit AI dashboard.

Register format:
/register COMPANY_NAME PHONE_NUMBER

Example:
/register AyamSerayu 0123456789

Commands:
/status - Check bot status
/users - View registered companies
/latest - View latest AI restocking recommendation
/help - Show command list
        """
    )


@bot.message_handler(commands=["help"])
def help_command(message):
    bot.reply_to(
        message,
        """
Available Commands:

/register COMPANY_NAME PHONE_NUMBER
Register your company to receive AI alerts.

/status
Check whether the bot is running.

/users
Show registered companies.

/latest
View latest AI restocking recommendation generated by Streamlit.
        """
    )


@bot.message_handler(commands=["register"])
def register(message):
    try:
        parts = message.text.split()

        if len(parts) < 3:
            bot.reply_to(
                message,
                "Invalid format.\n\nExample:\n/register AyamSerayu 0123456789"
            )
            return

        company_name = parts[1]
        phone = parts[2]

        users = load_users()

        users[company_name] = {
            "phone": phone,
            "chat_id": message.chat.id,
            "telegram_name": message.from_user.first_name
        }

        save_users(users)

        bot.reply_to(
            message,
            f"""
✅ Registration Successful

Company:
{company_name}

Phone:
{phone}

Telegram Chat ID:
{message.chat.id}

You will now receive AI seasonal procurement and restocking alerts from the Streamlit dashboard.
            """
        )

    except Exception as e:
        bot.reply_to(message, f"Registration failed: {e}")


@bot.message_handler(commands=["status"])
def status(message):
    bot.reply_to(
        message,
        "✅ AI Procurement Bot is active and ready to receive dashboard alerts."
    )


@bot.message_handler(commands=["users"])
def users_command(message):
    users = load_users()

    if len(users) == 0:
        bot.reply_to(message, "No registered companies yet.")
        return

    reply = "📌 Registered Companies:\n\n"

    for company, info in users.items():
        reply += f"- {company} | Phone: {info.get('phone')}\n"

    bot.reply_to(message, reply)


@bot.message_handler(commands=["latest"])
def latest(message):
    alert = load_latest_restocking_alert()

    if alert is None:
        bot.reply_to(
            message,
            "No latest AI restocking alert found yet. Generate one in Streamlit first."
        )
        return

    recommendation_lines = ""

    for item in alert.get("recommendations", []):
        recommendation_lines += (
            f"\n{item.get('Ingredient')}\n"
            f"Current Stock: {item.get('Current Stock')}\n"
            f"AI Demand: {item.get('AI Predicted Monthly Demand')}\n"
            f"Restock: +{item.get('Recommended Restock Quantity')}\n"
            f"Priority: {item.get('Priority')}\n"
        )

    reply = f"""
🤖 Latest AI Restocking Recommendation

Company:
{alert.get('company')}

Forecast:
{alert.get('forecast_month')}/{alert.get('forecast_year')}

Detected Event:
{alert.get('seasonal_event')}

Seasonal Confidence:
{alert.get('seasonal_confidence')}%

Suggested Restocking Date:
{alert.get('suggested_restock_date')}

Recommendations:
{recommendation_lines}

Main Suggestion:
{alert.get('main_suggestion')}
    """

    bot.reply_to(message, reply)


# =========================
# RUN BOT
# =========================
print("Telegram AI Procurement Bot is running...")
bot.infinity_polling()
